// Assignment operator//
public class operator {
    public static void main(String[] args){
        int a=5;
        int variable;
        variable=a;
        System.out.println("var value:"+variable);
        variable+=a;
        System.out.println("var value"+variable);
        variable*=a;
        System.out.println("var value:"+variable);
        variable-=a;
        System.out.println("var value:"+variable);

    }
}

