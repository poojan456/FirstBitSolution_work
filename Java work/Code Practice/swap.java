package Other;
public class swap {
    public static void main(String[] args) {
        int a = 20;
        int b = 30;
        int t;
        t = a;
        a = b;
        b = t;
        System.out.println("the value of a is" + a);
        System.out.println("the value of b is" + b);
    }
}
