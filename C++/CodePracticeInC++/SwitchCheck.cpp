#include<iostream>
using namespace std;
int main(){
	
	int day;
	cout<<"Enter day";
	cin>>day;
	switch(day){
		
		case 1:  
		
		    cout<<"monday";
		    break;
		    
	    case 2:
	    	cout<<"tuesday";
	    	break;
	    
	    case 3:
	    	cout<<"wensday";
	    	break;
	    case 4:
	    	cout<<"thursday";
	        break;
	    default :
	    	cout<<"invalid day";
		
		
	}
	
	return 0;
	
}
